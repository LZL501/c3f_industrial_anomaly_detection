class FPN(nn.Module):
    def __init__(self, f1_channels, f2_channels, f3_channels, f4_channels):
        super(FPN, self).__init__()

        self.P4_1 = nn.Conv2d(f4_channels, f3_channels, kernel_size=1, stride=1, padding=0)
        self.P4_upsampled = nn.Upsample(scale_factor=2, mode='bilinear')
        self.P4_2 = nn.Conv2d(f3_channels, f3_channels, kernel_size=5, stride=1, padding=2)

        self.P3_1 = nn.Conv2d(f3_channels, f2_channels, kernel_size=1, stride=1, padding=0)
        self.P3_upsampled = nn.Upsample(scale_factor=2, mode='bilinear')
        self.P3_2 = nn.Conv2d(f2_channels, f2_channels, kernel_size=5, stride=1, padding=2)

        self.P2_1 = nn.Conv2d(f2_channels, f1_channels, kernel_size=1, stride=1, padding=0)
        self.P2_upsampled = nn.Upsample(scale_factor=2, mode='bilinear')
        self.P2_2 =nn.Conv2d(f1_channels, f1_channels, kernel_size=3, stride=1, padding=1)
        
        # self.P1_2 = nn.Conv2d(f1_channels, f1_channels, kernel_size=3, stride=1, padding=1)


    def forward(self, inputs):
        f1, f2, f3, f4 = inputs

        f3_x = self.P4_1(f4)
        f3_x = self.P4_upsampled(f3_x)
        f3_x = f3 + f3_x
        f3_x = self.P4_2(f3_x)

        f2_x = self.P3_1(f3_x)
        f2_x = self.P3_upsampled(f2_x)
        f2_x = f2 + f2_x
        f2_x = self.P3_2(f2_x)

        f1_x = self.P2_1(f2_x)
        f1_x = self.P2_upsampled(f1_x)
        f1_x = f1 + f1_x
        f1_x = self.P2_2(f1_x)

        return f1_x, f2_x, f3_x, f4